            void mostrardatos(String valor){
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("ID");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("APELLIDO");
        modelo.addColumn("DUI");
        modelo.addColumn("CELULAR");
        modelo.addColumn("TELEFONO");
        modelo.addColumn("DIRECCION");
        modelo.addColumn("EDAD");
        modelo.addColumn("NACIMIENTO");
        modelo.addColumn("REGISTRO");
        registro.setModel(modelo);
        String sql="";
        if (valor.equals(""))
                {
                    sql="SELECT id, nombre_c, apellido_c, dui_c, celular_c, telefonocasa_c, direccion_c, edad_c, fnacimiento_c, fregistro_c FROM cliente";
            }
        else {
                sql="SELECT id, nombre_c, apellido_c, dui_c, celular_c, telefonocasa_c, direccion_c, edad_c, fnacimiento_c, fregistro_c FROM cliente WHERE id='"+valor+"'";
            }
        
         String []datos = new String [10];
        try {
            Statement st = cn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                while(rs.next()){
                   datos[0] = (String.valueOf(rs.getString(1)));
                    datos[1]= rs.getString(2);
                    datos[2]= rs.getString(3);
                    datos[3]= rs.getString(4);
                    datos[4]= rs.getString(5);
                    datos[5]= rs.getString(6);
                    datos[6]= rs.getString(7);
                     datos[7]= rs.getString(8);
                     datos[8]= rs.getString(9);
                     datos[9]= rs.getString(10);
                    
                   
                   modelo.addRow(datos);
                }
                registro.setModel(modelo);
        } catch (SQLException ex) {
            
        }
            }
