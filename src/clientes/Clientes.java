            PreparedStatement pst = cn.prepareStatement("INSERT INTO cliente (nombre_c, apellido_c, dui_c, celular_c, telefonocasa_c, direccion_c, edad_c,fnacimiento_c, fregistro_c) VALUES(?,?,?,?,?,?,?,?,?)");
               
                
