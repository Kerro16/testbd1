                pst.setString(2, txtapellido.getText());
                pst.setString(3, txtdui.getText());
                pst.setString(4, txtcelular.getText());
                pst.setString(5, txttelefono.getText());
                pst.setString(6, txtdireccion.getText());
                pst.setString(7, txtedad.getText());
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String date = sdf.format(jdnacimiento.getDate());
                pst.setString(8,date);   
                SimpleDateFormat sdfg = new SimpleDateFormat("yyyy-MM-dd");
                String date1 = sdf.format(jdregistro.getDate());
                pst.setString(9,date1);
               
                
               
                pst.executeUpdate();
                mostrardatos("");
