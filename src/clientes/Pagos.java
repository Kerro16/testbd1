    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            PreparedStatement pst = cn.prepareStatement("INSERT INTO pago (codcliente, montopago, fechapago, mespago) VALUES(?,?,?,?)");
               
                
                pst.setString(1, txtid.getText());
                pst.setString(2, txtpago.getText());
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String date = sdf.format(jdfecha.getDate());
                pst.setString(3,date);
                pst.setString(4, txtmes.getText());
                
                 
                
               
                
               
                pst.executeUpdate();
                mostrardatos("");
                JOptionPane.showMessageDialog(null, "Pago Efectivo");
                
        } catch (SQLException ex) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "No Hay Pago");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

