            void mostrardatos(String valor){
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("ID");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("APELLIDO");
        modelo.addColumn("DUI");
        registro.setModel(modelo);
        String sql="";
        if (valor.equals(""))
                {
                    sql="SELECT id, nombre_c, apellido_c, dui_c FROM cliente";
            }
        else {
                sql="SELECT id, nombre_c, apellido_c, dui_c FROM cliente WHERE id='"+valor+"'";
            }
        
         String []datos = new String [5];
        try {
            Statement st = cn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                while(rs.next()){
                   datos[0] = (String.valueOf(rs.getString(1)));
                    datos[1]= (String.valueOf(rs.getString(2)));
                    datos[2]= (String.valueOf(rs.getString(3)));
                    datos[3]= (String.valueOf(rs.getString(4)));
                    
                   
                   modelo.addRow(datos);
                }
                registro.setModel(modelo);
        } catch (SQLException ex) {
            
        }
            }
